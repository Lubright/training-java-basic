package com.examples;

public class Mathpublic {

	public static void main(String[] args) {
		
		System.out.println("-5的絕對值是: " + Math.abs(-5));
		System.out.println("1.2, 3.4 比較大的是: " + Math.max(1.2, 3.4));
		System.out.println("1.2, 3.4 比較小的是: " + Math.min(1.2, 3.4));
		System.out.println("2的3次方是: " + Math.pow(2, 3));
		System.out.println("sqrt(5): " + Math.sqrt(5));
		System.out.println("round(3.7): " + Math.round(3.7));
		System.out.println("ceil(3.7): " + Math.ceil(3.7));
		System.out.println("floor(3.7): " + Math.floor(3.7));
		
	}

}
